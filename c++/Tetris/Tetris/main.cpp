// 메뉴 선택
MENU ReadyGame()
{
	int y = 0;
	int input = 0;

	GameManager* menuGameManager = new GameManager();

	while (true) {

		menuGameManager->DrawMainScreen();
		menuGameManager->DrawUserCursor(y);
		input = _getch();

		if (input == MAGIC_KEY)
		{
			switch (_getch())
			{
			case UP:
				--y;
				break;
			case DOWN:
				++y;
				break;
			}
		}
		else if (input == SPACEBAR)
		{
			switch (y)
			{
			case 0:
				delete menuGameManager;
				return GAMESTART;
			case 1:
				delete menuGameManager;
				return GAMEINFO;
			case 2:
				delete menuGameManager;
				return QUIT;
			}
		}
	}
}

